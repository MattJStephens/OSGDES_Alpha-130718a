#include "LogFile.h"


void CLogFile::create(bool active, bool close_after_append, std::string file_name)
{
	m_active = active;
	m_close_after_append = close_after_append;
	m_file_name = file_name;
	if(m_active)
	{
		if(m_close_after_append)
		{
			m_ofs_alpha.open(m_file_name);
		}
	}

}

void CLogFile::write(std::string message)
{
	if(m_active)
	{
		if(m_close_after_append)
		{
			std::fstream ofs_beta;
			ofs_beta.open(m_file_name, std::ios_base::out | std::ios_base::app);
			ofs_beta << message << "\n";
			ofs_beta.close();
		}
		else
		{
			m_ofs_alpha << message;
		}
	}

}


	
void CLogFile::close()
{
	if(!m_close_after_append)
	{
		m_ofs_alpha.close();
	}
}